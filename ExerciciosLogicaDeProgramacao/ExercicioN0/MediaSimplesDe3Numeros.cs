using System;
using System.Collections.Generic;
using System.Text;

namespace ExerciciosLogicaDeProgramacao.ExercicioN0
{
    public class MediaSimplesDe3Numeros
    {
        public int CalcularMedia(int a, int b, int c)
        {
            var media = (a + b + c) / 3;
            return media;
        }
    }
