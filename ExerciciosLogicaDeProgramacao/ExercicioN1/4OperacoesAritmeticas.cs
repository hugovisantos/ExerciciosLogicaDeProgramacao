using System;
using System.Collections.Generic;
using System.Text;

namespace ExerciciosLogicaDeProgramacao.ExercicioN1
{
    public class _4OperacoesAritmeticas
    {
        public int Adicao(int numero1, int numero2)
        {
            var resultado = numero1 + numero2;
            return resultado;
        }
        public int Subtracao(int numero1, int numero2)
        {
            var resultado = numero1 - numero2;
            return resultado;
        }
        public int Multiplicacao(int numero1, int numero2)
        {
            var resultado = numero1 * numero2;
            return resultado;
        }
        public int Divisao(int numero1, int numero2)
        {
            var resultado = numero1 / numero2;
            return resultado;
        }

    }
}
