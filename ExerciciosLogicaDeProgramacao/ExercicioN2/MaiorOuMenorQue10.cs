using System;
using System.Collections.Generic;
using System.Text;

namespace ExerciciosLogicaDeProgramacao.ExercicioN2
{
    public class MaiorOuMenorQue10
    {
        public bool MaiorQue10(int valor)
        {
            return valor > 10;
        }
        public bool MenorQue10(int valor)
        {
            return valor < 10;
        }
        public bool IgualA10(int valor)
        {
            return valor == 10;
        }
    }
}
